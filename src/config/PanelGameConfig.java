package config;

public class PanelGameConfig {
	private String className;
	
	private String imgBackground;
	
	private String imgReturn;
	
	private String imgClose;
	
	public PanelGameConfig(){
		
	}

	public String getClassName() {
		return className;
	}

	public String getImgBackground() {
		return imgBackground;
	}

	public String getImgReturn() {
		return imgReturn;
	}

	public String getImgClose() {
		return imgClose;
	}
}
