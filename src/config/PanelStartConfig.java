package config;

public class PanelStartConfig {
	private String className;
	
	private String imgBackground;
	
	private String imgNewGame;
	
	private String imgConfig;
	
	private String imgHelp;
	
	private String imgQuit;
	
	public PanelStartConfig(){
		
	}

	public String getClassName() {
		return className;
	}

	public String getImgBackground() {
		return imgBackground;
	}

	public String getImgNewGame() {
		return imgNewGame;
	}

	public String getImgConfig() {
		return imgConfig;
	}

	public String getImgHelp() {
		return imgHelp;
	}

	public String getImgQuit() {
		return imgQuit;
	}
}
